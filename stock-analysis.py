import yfinance as yf
import google.generativeai as genai
from google.colab import userdata

# ==========================================
# ส่วนที่ 1: ส่วนเชื่อมต่อสมอง (API Connection)
# ==========================================
# ส่วนนี้ทำหน้าที่เชื่อมต่อกับกุญแจ API เพื่อให้เข้าถึงสมอง AI ได้
api_key = userdata.get('MY_API_KEY')
genai.configure(api_key=api_key)

# ==========================================
# ส่วนที่ 2: ส่วนตั้งค่าสมองและเพอร์โซน่า (Model & Persona)
# ==========================================
# ส่วนนี้ทำหน้าที่สร้าง "ตัวตน" ของ AI ว่าต้องเก่งเรื่องอะไรและมีนิสัยแบบไหน
model = genai.GenerativeModel(
    model_name='gemini-1.5-flash',
    system_instruction="""คุณคือนักวิเคราะห์การเงินมืออาชีพ 
    จงตอบคำถามด้วยความแม่นยำ กระชับ และเน้นข้อมูลที่เป็นประโยชน์แก่นักลงทุน"""
)

# ==========================================
# ส่วนที่ 3: ส่วนรับคำสั่งจากผู้ใช้งาน (Input Handler)
# ==========================================
# ส่วนนี้ให้ไอด้าพิมพ์หุ้นที่อยากรู้ และคำถามที่อยากถาม
print("=== ระบบวิเคราะห์หุ้นส่วนตัวของไอด้า ===")
target_ticker = input("ระบุชื่อหุ้นที่ต้องการ (เช่น SMR, NVDA): ").upper()
user_question = input("อยากรู้อะไรเกี่ยวกับหุ้นตัวนี้เป็นพิเศษไหม?: ")

# ==========================================
# ส่วนที่ 4: ส่วนดึงข้อมูล (Data Fetching)
# ==========================================
# ส่วนนี้คือการไปหยิบข้อมูลดิบ (Data) จากโลกภายนอกมาให้ AI อ่าน
print(f"\nกำลังดึงข้อมูลของ: {target_ticker}...")
stock = yf.Ticker(target_ticker)
stock_info = stock.info

# ==========================================
# ส่วนที่ 5: ส่วนเตรียมคำสั่งถาม-ตอบ (Prompt Construction)
# ==========================================
# ส่วนนี้คือการเอาข้อมูลหุ้น + คำถามของไอด้า มาปั้นเป็นประโยคให้ AI เข้าใจ
prompt = f"""
วิเคราะห์หุ้น {target_ticker} ให้หน่อย โดยมีข้อมูลดังนี้:
- กลุ่มอุตสาหกรรม: {stock_info.get('sector', 'N/A')}
- ค่า P/E Ratio: {stock_info.get('trailingPE', 'N/A')}
- มูลค่าตลาด: {stock_info.get('marketCap', 'N/A')}

คำถามจากผู้ใช้: {user_question}
"""

# ==========================================
# ส่วนที่ 6: ส่วนประมวลผลและแสดงผล (AI Analysis & Output)
# ==========================================
# ส่วนนี้คือการส่งคำสั่งไปให้สมอง AI แล้วรอรับคำตอบออกมาแสดง
response = model.generate_content(prompt)
print("\n--- ผลการวิเคราะห์จาก AI ---")
print(response.text)


